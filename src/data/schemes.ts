// ── helpers ─────────────────────────────────────────────────
export const getSchemeById = (id: string): Scheme | undefined =>
  schemes.find((s) => s.id === id);

export const getSchemesByCategory = (category: SchemeCategory | "all"): Scheme[] =>
  category === "all" ? schemes : schemes.filter((s) => s.category === category);

export const searchSchemes = (query: string): Scheme[] => {
  const q = query.toLowerCase().trim();
  if (!q) return schemes;
  return schemes.filter(
    (s) =>
      s.name.toLowerCase().includes(q) ||
      s.nameHi.includes(q) ||
      s.tags.some((t) => t.toLowerCase().includes(q)) ||
      s.overview.toLowerCase().includes(q) ||
      s.category.includes(q)
  );
};
